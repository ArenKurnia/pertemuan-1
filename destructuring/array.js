// Array Destructuring

let array1 = [2, 3, 4];
let a,b,c;
[a,b,c] = array1;
console.log(a);
//output : 2
console.log(b);
//output : 3
console.log(c);
//output : 4
console.log(a, b, c);
//output : 2 3 4
console.log(a + "," + b + "," + c);
//output : 2,3,4
console.log(`${a},${b},${c}`);
//output : 2, 3, 4
